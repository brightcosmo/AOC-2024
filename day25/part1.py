import os.path

INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def solve(s: str) -> int:
    lines = s.splitlines()
    for line in lines:
        print(line)
    
    # SOLUTION
    # Return the number of lock/key pairs that fit together without overlap.
    grids = s.strip().split("\n\n")
    items = [{i for i, c in enumerate(item) if c == '#'} for item in grids]
    return sum(not k & l for k in items for l in items) // 2

